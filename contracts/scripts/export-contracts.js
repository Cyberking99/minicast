const fs = require("fs");
const path = require("path");
require("dotenv").config({ path: path.join(__dirname, "../../.env") });

// Deployed addresses on local Anvil node (chainId 31337)
const LOCAL_PREDICTION_POOL = "0xD5ac451B0c50B9476107823Af206eD814a2e2580";
const LOCAL_USDC = "0x18E317A7D70d8fBf8e6E893616b52390EbBdb629";

// Base Sepolia addresses
const BASE_SEPOLIA_PREDICTION_POOL = process.env.NEXT_PUBLIC_PREDICTION_POOL_ADDRESS ?? "0x0000000000000000000000000000000000000000";
const BASE_SEPOLIA_USDC = process.env.NEXT_PUBLIC_USDC_ADDRESS ?? "0x036CbD53842c5426634e7929541eC2318f3dCF7e";

function main() {
  const artifactsDir = path.join(__dirname, "../artifacts/src");
  
  const poolJsonPath = path.join(artifactsDir, "PredictionPool.sol/PredictionPool.json");
  const usdcJsonPath = path.join(artifactsDir, "mocks/MockUSDC.sol/MockUSDC.json");

  if (!fs.existsSync(poolJsonPath) || !fs.existsSync(usdcJsonPath)) {
    console.error("Artifacts not found. Run npx hardhat compile first.");
    process.exit(1);
  }

  const poolArtifact = JSON.parse(fs.readFileSync(poolJsonPath, "utf8"));
  const usdcArtifact = JSON.parse(fs.readFileSync(usdcJsonPath, "utf8"));

  const targetFile = path.join(__dirname, "../../frontend/src/shared/lib/contracts.ts");

  const fileContent = `// This file is auto-generated by contracts/scripts/export-contracts.js. Do not edit manually.

import { createPublicClient, http } from 'viem';
import { baseSepolia, localhost } from 'viem/chains';

export const CONTRACT_ADDRESSES = {
  31337: {
    predictionPool: "${LOCAL_PREDICTION_POOL}",
    usdc: "${LOCAL_USDC}",
    deployedBlock: 0n,
  },
  84532: {
    predictionPool: "${BASE_SEPOLIA_PREDICTION_POOL}",
    usdc: "${BASE_SEPOLIA_USDC}",
    deployedBlock: 42799516n,
  },
} as const;

export const PREDICTION_POOL_ABI = ${JSON.stringify(poolArtifact.abi, null, 2)} as const;

export const USDC_ABI = ${JSON.stringify(usdcArtifact.abi, null, 2)} as const;

const chainId = Number(process.env.NEXT_PUBLIC_CHAIN_ID || 31337) as 31337 | 84532;
const chain = chainId === 84532 ? baseSepolia : localhost;
const rpcUrl = process.env.NEXT_PUBLIC_RPC_URL || (chainId === 31337 ? 'http://127.0.0.1:8545' : 'https://sepolia.base.org');

export const publicClient = createPublicClient({
  chain,
  transport: http(rpcUrl),
});

export const getPredictionPoolAddress = () => CONTRACT_ADDRESSES[chainId].predictionPool;
export const getUsdcAddress = () => CONTRACT_ADDRESSES[chainId].usdc;

export async function fetchOnChainPools() {
  const address = getPredictionPoolAddress();
  
  try {
    const startBlock = CONTRACT_ADDRESSES[chainId].deployedBlock;
    const latest = await publicClient.getBlockNumber();
    
    // Base Sepolia RPC has a query range limit of 2000 blocks
    const batchSize = 2000n;
    const promises = [];
    
    for (let from = startBlock; from <= latest; from += batchSize) {
      const to = from + batchSize - 1n > latest ? latest : from + batchSize - 1n;
      promises.push(
        publicClient.getContractEvents({
          address,
          abi: PREDICTION_POOL_ABI,
          eventName: 'PoolCreated',
          fromBlock: from,
          toBlock: to,
        }).catch(err => {
          console.warn(\`Failed to fetch events from \${from} to \${to}:\`, err);
          return [];
        })
      );
    }

    const results = await Promise.all(promises);
    const logs = results.flat();
    const poolIds = logs.map(log => log.args.poolId).filter(Boolean) as string[];

    if (poolIds.length === 0) {
      return [];
    }

    const poolDetailsCalls = poolIds.flatMap(poolId => [
      {
        address,
        abi: PREDICTION_POOL_ABI,
        functionName: 'pools',
        args: [poolId],
      },
      {
        address,
        abi: PREDICTION_POOL_ABI,
        functionName: 'getPoolOptions',
        args: [poolId],
      }
    ]);

    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    const detailsResults = await publicClient.multicall({ contracts: poolDetailsCalls as any });

    const pools = [];

    for (let i = 0; i < poolIds.length; i++) {
      const poolId = poolIds[i];
      const poolResult = detailsResults[i * 2];
      const optionsResult = detailsResults[i * 2 + 1];

      if (poolResult.status === 'success' && optionsResult.status === 'success') {
        // eslint-disable-next-line @typescript-eslint/no-explicit-any
        const poolData = poolResult.result as any;
        const options = optionsResult.result as string[];

        const optionTotalCalls = options.map((_, optIdx) => ({
          address,
          abi: PREDICTION_POOL_ABI,
          functionName: 'optionTotals',
          args: [poolId, optIdx],
        }));

        // eslint-disable-next-line @typescript-eslint/no-explicit-any
        const totalsResults = await publicClient.multicall({ contracts: optionTotalCalls as any });

        const parsedOptions = options.map((label, optIdx) => {
          const totalStakedRaw = totalsResults[optIdx]?.status === 'success' ? (totalsResults[optIdx].result as bigint) : BigInt(0);
          const totalStaked = Number(totalStakedRaw) / 10 ** 6;
          return {
            label,
            totalStakedRaw,
            totalStaked,
          };
        });

        const totalPoolRaw = poolData[8] as bigint;
        const totalPool = Number(totalPoolRaw) / 10 ** 6;

        const formattedOptions = parsedOptions.map(opt => {
          const percentage = totalPool > 0 ? Math.round((opt.totalStaked / totalPool) * 100) : 0;
          return {
            label: opt.label,
            percentage,
            totalStakedStr: \`\$\${opt.totalStaked.toLocaleString(undefined, { minimumFractionDigits: 1, maximumFractionDigits: 1 })}\`,
            stakers: 0,
          };
        });

        const statusMap = ["open", "locked", "resolving", "settled"];
        const statusUint = poolData[7] as number;
        const status = statusMap[statusUint] || "open";

        const stakeDeadline = Number(poolData[2]);
        const now = Math.floor(Date.now() / 1000);
        const diffSecs = stakeDeadline - now;
        let timeLeft = "Closed";
        if (diffSecs > 0) {
          const days = Math.floor(diffSecs / (24 * 3600));
          const hours = Math.floor((diffSecs % (24 * 3600)) / 3600);
          const mins = Math.floor((diffSecs % 3600) / 60);
          if (days > 0) {
            timeLeft = \`\${days}d \${hours}h\`;
          } else if (hours > 0) {
            timeLeft = \`\${hours}h \${mins}m\`;
          } else {
            timeLeft = \`\${mins}m\`;
          }
        }

        pools.push({
          id: poolId,
          question: poolData[1] as string,
          category: "General",
          status: status as "open" | "locked" | "resolving" | "settled",
          thumbnailUrl: \`https://picsum.photos/seed/\${poolId.slice(0, 10)}/176/132\`,
          poolTotal: \`\$\${totalPool.toLocaleString(undefined, { minimumFractionDigits: 1, maximumFractionDigits: 1 })}\`,
          timeLeft,
          totalStakers: 0,
          options: formattedOptions,
        });
      }
    }

    return pools;
  } catch (error) {
    console.error("Error fetching pools from contract:", error);
    return [];
  }
}
`;

  // Ensure target directory exists
  const targetDir = path.dirname(targetFile);
  if (!fs.existsSync(targetDir)) {
    fs.mkdirSync(targetDir, { recursive: true });
  }

  fs.writeFileSync(targetFile, fileContent, "utf8");
  console.log("Contracts exported successfully to:", targetFile);
}

main();
